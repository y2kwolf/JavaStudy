public class SwitchCase {

	public static void main(String[] args) {
		char select = 'd';
		
		switch(select) {
		case 'a':
			System.out.println("a");
			break;
		case 'b':
			System.out.println("b");
			break;
		case 'c':
			System.out.println("c");
			break;
		case 'd':
			System.out.println("d");
			break;
		}

	}

}
